package com.syntax.class03;

public class RecapOperations {

	public static void main(String[] args) {
		
		
		/*
		 * Addition => +
		 * Subtraction => -
		 * Multiplication => *
		 * Division => /
		 * Modulus/Remainder => %
		 */
		
		
		int number1= 11; //3+3+3 =9; 9/3=3 2=>remainder from 11;
		int number2=3;
		System.out.println("Answer=" +number1 / number2);
		System.out.println("Remainder=" +number1 % number2);

		
		double number3= 9;
		double number4=2;
		System.out.println(number3/ number4);
		
		
		float number5= 1f;
		float number6=3f;
		System.out.println(number5/ number6);
		
		
		double number7= 1;
		double number8=3;
		System.out.println(number7/ number8);
		
	}

}
